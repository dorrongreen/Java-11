public class MoreWhileLoops {
    public static void main(String[] args) {
        int count = 0;
        int count2 = 0;
        int count3 = 7;

        while(count < 10){
            System.out.printf("count is %d\n", count);
            count++;
        }

        while(count2 > -10){
            System.out.printf("count from while loop 2 is %d\n", count2);
            count2--;
        }

        while(count3 > 2){
            System.out.printf("count from while loop 2 is %d\n", count3);
            count3--;
        }
    }
}
