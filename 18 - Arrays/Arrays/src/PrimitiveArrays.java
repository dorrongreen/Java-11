public class PrimitiveArrays {
    public static void main(String[] args) {
        /*
         * Refer to Oracle Documentation on Primitive Data Types
         * 
         * 
         */

        int[] numbers = new int[3];

        numbers[0] = 9;
        numbers[1]= 47;
        numbers[2] = 87;


        for(int i = 0; i<3; i++){
            System.out.println(numbers[i]);
        }
        

    }
}
