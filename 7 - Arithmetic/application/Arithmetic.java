package application;

public class Arithmetic {
    
    public static void main(String[] args) {
        // Plus +
        // Divide /
        // Multiply *
        // Subtraction -
        
        System.out.println(9+8);
        
        System.out.println(27/3);

        System.out.println(10/3);

        System.out.println(10.0/3.0);
        System.out.println(9/4.0);

        System.out.println(3*4);

        System.out.println(3-4);

    }

}